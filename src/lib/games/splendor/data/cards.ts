import type { Card, Gem, GemCost, Tier } from '../state';

type RawCard = [bonus: Gem, prestige: number, cost: GemCost];

function makeCards(tier: Tier, rows: RawCard[]): Card[] {
  return rows.map(([bonus, prestige, cost], index) => ({
    id: `t${tier}_${String(index + 1).padStart(2, '0')}`,
    tier,
    bonus,
    prestige,
    cost,
  }));
}

